#################################################################
# Function Name : Add
# Description   : Accepts two numbers and returns their addition.
# Input         : Integer, Integer
# Output        : Integer
#################################################################
def Add(No1, No2):
    return No1 + No2


#################################################################
# Function Name : Sub
# Description   : Accepts two numbers and returns their subtraction.
# Input         : Integer, Integer
# Output        : Integer
#################################################################
def Sub(No1, No2):
    return No1 - No2


#################################################################
# Function Name : Mul
# Description   : Accepts two numbers and returns their multiplication.
# Input         : Integer, Integer
# Output        : Integer
#################################################################
def Mult(No1, No2):
    return No1 * No2


#################################################################
# Function Name : Div
# Description   : Accepts two numbers and returns their division.
# Input         : Integer, Integer
# Output        : Float
#################################################################
def Div(No1, No2):
    return No1 / No2
